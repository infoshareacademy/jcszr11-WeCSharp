function DeleteCalendarEvent(actionUrl) {
    Swal.fire({
        title: 'Confirm Deletion',
        text: 'Are you sure you want to delete your calendar event?',
        icon: 'question',
        showCancelButton: true
    }).then(result => {
        if (result.isConfirmed) {
            $.ajax({
                url: actionUrl,
                type: 'POST',
                success: function (result) {
                    localStorage.setItem('calendarEventDeletedMessage', 'Your calendar event has been deleted successfully');
                    location.reload();
                },
                error: function (error) {
                    alert("failure");
                }
            });

        } else if (result.dismiss === Swal.DismissReason.cancel) {
            Swal.fire('Cancelled', 'operation has been canceled', 'info');
        }
    });
}